        us.setPass(String.valueOf(txtPass.getText()));
        us.setTip(cbxTipo.getSelectedIndex()+1);
        us.comparadorUser();
        if(us.isA()==false){
           us.insertar();
        }else{
            JOptionPane.showMessageDialog(null, "Existe un Usuario igual");
            txtUser.setText(null);
            txtPass.setText(null);
            txtUser.requestFocus();
        }
